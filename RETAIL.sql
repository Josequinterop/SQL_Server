-- Client
CREATE TABLE Client.Clientes (
    periodo VARCHAR(6),
    customer_id INT,
    negocio VARCHAR(50),
    tipo_negocio VARCHAR(50),
    PRIMARY KEY (periodo, customer_id)
);

-- Prod
CREATE TABLE Prod.Producto (
    periodo VARCHAR(6),
    id_producto INT,
    id_familia INT,
    id_categoria INT,
    tier_id INT,
    costo DECIMAL(18, 0),
    PRIMARY KEY (periodo, id_producto)
);
-- Sales
CREATE TABLE Sales.Delivery (
    periodo VARCHAR(6),
    nro_orden INT,
    fecha_delivery DATE,
    PRIMARY KEY (nro_orden, periodo),
	
);


CREATE TABLE Sales.Ventas (
	periodo VARCHAR(6),
    fecha_compra DATE,
    nro_orden INT,
    customer_id INT,
    id_producto INT,
    canal_venta CHAR(1),
    cantidad_venta INT,
    precio_unitario DECIMAL(18, 10),
    peso DECIMAL(18, 10),
	PRIMARY KEY (periodo, nro_orden),
	FOREIGN KEY (nro_orden,periodo) REFERENCES Sales.Delivery  (nro_orden,periodo),
	FOREIGN KEY (periodo,id_producto) REFERENCES Prod.Producto  (periodo,id_producto),
	FOREIGN KEY (periodo, customer_id) REFERENCES Client.Clientes  (periodo, customer_id)
);



